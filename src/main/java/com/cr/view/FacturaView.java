    private void cbAparatoItemStateChanged(java.awt.event.ItemEvent evt) {// GEN-FIRST:event_cbAparatoItemStateChanged
        // TODO add your handling code here:
        if (evt.getSource() == cbAparato) {

            switch (cbAparato.getSelectedIndex()) {
            case 0:
                this.txtCds.setEnabled(true);
                this.txtCapacidad.setEnabled(false);
                this.txtTamanio.setEnabled(false);
                break;
            case 1:
                this.txtCds.setEnabled(false);
                this.txtCapacidad.setEnabled(true);
                this.txtTamanio.setEnabled(false);
                break;
            case 2:
                this.txtCds.setEnabled(false);
                this.txtCapacidad.setEnabled(false);
                this.txtTamanio.setEnabled(true);
                break;
            }
        }

    }// GEN-LAST:event_cbAparatoItemStateChanged

    private void limpiar(JTextField... txt) {

        for (JTextField t : txt) {
            t.setText("");
        }
    }

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {

        System.out.println("aqui");

        FacturaFactory factFactory = new FacturaFactory();
        AparatoFactory aparFactory = new AparatoFactory();
        Factura factura = factFactory.crearFactura(cbAparato.getSelectedIndex());
        Aparato aparato = aparFactory.crearAparato(cbAparato.getSelectedIndex());
        aparato.establecerVoltaje(Integer.parseInt(txtVoltaje.getText()));
        aparato.establecerPrecio(Double.parseDouble(txtPrecio.getText()));
        int numero = Integer.parseInt(txtNumero.getText());
        double descuento = Double.parseDouble(txtDescuento.getText());
        switch (cbAparato.getSelectedIndex()) {
        case 0:
            FacturaEquipo fact = (FacturaEquipo) factura;
            Equipo equipo = (Equipo) aparato;
            equipo.establecerCantidadCDs(Integer.parseInt(txtCds.getText()));
            fact.establecer(numero, equipo, descuento);
            controller.agregar(fact);
            break;
        case 1:
            FacturaIPod fact1 = (FacturaIPod) factura;
            IPod ipod = (IPod) aparato;
            ipod.establecerCapacidad(Double.parseDouble(txtCapacidad.getText()));
            fact1.establecer(numero, ipod, descuento);
            controller.agregar(fact1);
            break;
        case 2:
            FacturaTV fact2 = (FacturaTV) factura;
            Televisor tv = (Televisor) aparato;
            tv.establecerTamanio(Integer.parseInt(txtTamanio.getText()));
            fact2.establecer(numero, tv, descuento);
            controller.agregar(fact2);
            break;

        }
        setTableModel();
        limpiar(txtNumero,txtDescuento,txtPrecio,txtVoltaje,txtCapacidad,txtCds,txtTamanio);

        for (Factura it : controller.Listar()) {
            System.out.println(it);
        }
    }
    

